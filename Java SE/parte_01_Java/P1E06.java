package parte_01_Java;

public class P1E06 {
    public static void main(String[] args){
        double pi = 3.14159;
        double raio = 2.0;
        double perimetro = 2 * pi * raio;
        double area = pi * raio * raio;

        System.out.println ("A Área é igual a " + area);
        System.out.println ("O Perímetro é igual a " + perimetro);
    }
}
